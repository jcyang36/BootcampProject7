package diamond;
public class EscapeSequences
 {
     public static void main( String[] args)
    {

       
        System.out.println("*********\r\n");
        System.out.println("*\t*\r\n");
        System.out.println("*\t*\r\n");
        System.out.println("*\t*\r\n");
        System.out.println("*\t*\r\n");
		System.out.println("*********\r");
		
     }
 }